def full_counting_sort_prep(array)
  counts = get_counts(array)
  counts.each_with_index do |element, index|
    counts[index] += counts[index-1] unless index.zero?
  end
  counts
end

def get_counts(array)
  array.each_with_object(Array.new(100,0)) { |element,counts| counts[element.to_i] += 1 }
end

